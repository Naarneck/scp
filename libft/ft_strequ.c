#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t i;

	if (s1 && s2)
	{
		i = 1;
		while (s1[i - 1] != '\0' && s1[i - 1] != '\0')
		{
			if (s1[i] != s2[i])
				return (0);
			i++;
		}
	}
	return (1);
}
