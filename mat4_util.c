#include "scop.h"

t_mat4		mat4_mult(t_mat4 a, t_mat4 b)
{
	t_mat4	m;
	int		i;

	i = 0;
	while (i < 4)
	{
		m.a[i][0] = a.a[i][0] * b.a[0][0] + a.a[i][1] * b.a[1][0]
		+ a.a[i][2] * b.a[2][0] + a.a[i][3] * b.a[3][0];
		m.a[i][1] = a.a[i][0] * b.a[0][1] + a.a[i][1] * b.a[1][1]
		+ a.a[i][2] * b.a[2][1] + a.a[i][3] * b.a[3][1];
		m.a[i][2] = a.a[i][0] * b.a[0][2] + a.a[i][1] * b.a[1][2]
		+ a.a[i][2] * b.a[2][2] + a.a[i][3] * b.a[3][2];
		m.a[i][3] = a.a[i][0] * b.a[0][3] + a.a[i][1] * b.a[1][3]
		+ a.a[i][2] * b.a[2][3] + a.a[i][3] * b.a[3][3];
		i++;
	}
	return (m);
}

t_mat4		mat4_identity(void)
{
	t_mat4	tr;

	tr.a[0][0] = 1.0f;
	tr.a[0][1] = 0.0f;
	tr.a[0][2] = 0.0f;
	tr.a[0][3] = 0.0f;
	tr.a[1][0] = 0.0f;
	tr.a[1][1] = 1.0f;
	tr.a[1][2] = 0.0f;
	tr.a[1][3] = 0.0f;
	tr.a[2][0] = 0.0f;
	tr.a[2][1] = 0.0f;
	tr.a[2][2] = 1.0f;
	tr.a[2][3] = 0.0f;
	tr.a[3][0] = 0.0f;
	tr.a[3][1] = 0.0f;
	tr.a[3][2] = 0.0f;
	tr.a[3][3] = 1.0f;
	return (tr);
}

t_mat4		mat4_transpose(t_mat4 src)
{
	t_mat4	tr;

	tr.a[0][0] = src.a[0][0];
	tr.a[0][1] = src.a[1][0];
	tr.a[0][2] = src.a[2][0];
	tr.a[0][3] = src.a[3][0];
	tr.a[1][0] = src.a[0][1];
	tr.a[1][1] = src.a[1][1];
	tr.a[1][2] = src.a[2][1];
	tr.a[1][3] = src.a[3][1];
	tr.a[2][0] = src.a[0][2];
	tr.a[2][1] = src.a[1][2];
	tr.a[2][2] = src.a[2][2];
	tr.a[2][3] = src.a[3][2];
	tr.a[3][0] = src.a[0][3];
	tr.a[3][1] = src.a[1][3];
	tr.a[3][2] = src.a[2][3];
	tr.a[3][3] = src.a[3][3];
	return (tr);
}
